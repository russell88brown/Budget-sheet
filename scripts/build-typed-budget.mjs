import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "esbuild";

const scriptPath = fileURLToPath(import.meta.url);
const repoRoot = path.resolve(path.dirname(scriptPath), "..");
const outFile = path.join(repoRoot, "src", "B08_TypedBudget.generated.gs");
const entryFile = path.join(repoRoot, "ts", "apps-script", "entry.ts");

const result = await build({
  entryPoints: [entryFile],
  bundle: true,
  format: "iife",
  globalName: "TypedBudget",
  target: "es2020",
  platform: "browser",
  minify: false,
  write: false,
});

if (!result.outputFiles || !result.outputFiles.length) {
  throw new Error("Typed build produced no output.");
}

const banner =
  "// AUTO-GENERATED FILE. DO NOT EDIT.\n" +
  "// Generated by: npm run build:typed\n";
const code = banner + result.outputFiles[0].text;

fs.writeFileSync(outFile, code, "utf8");
console.log(`Wrote ${path.relative(repoRoot, outFile)}`);
